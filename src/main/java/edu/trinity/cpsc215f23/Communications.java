package edu.trinity.cpsc215f23;

/**
 * A list of example communication options that a contact may have. Other options can be added.
 *
 * @version 1.0.0, 10th December 2023
 * @author Shivanshu Dwivedi
 */
public enum Communications {
    /**
     * Stores the email of the contact
     */
    EMAIL,
    /**
     * Stores the mobile of the contact
     */
    MOBILE,
    /**
     * Stores the github of the contact
     */
    GITHUB,
    /**
     * Stores the instagram of each contact
     */
    INSTAGRAM,
    /**
     * Stores the linkedin of each contact
     */
    LINKEDIN,
    /**
     * Stores the website of each contact
     */
    WEBSITE,
    /**
     * Stores the snapchat of each contact
     */
    SNAPCHAT
}
